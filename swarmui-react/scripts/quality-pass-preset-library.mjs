/**
 * Preserve insertion order while removing accidental duplicates.
 * @param {string[]} words
 */
function uniqueWords(words) {
  const seen = new Set();
  const result = [];
  for (const word of words) {
    const key = word.toLowerCase().trim();
    if (!key || seen.has(key)) {
      continue;
    }
    seen.add(key);
    result.push(word);
  }
  return result;
}

