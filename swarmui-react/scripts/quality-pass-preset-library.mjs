let updatedCount = 0;
for (const preset of presets) {
  const override = overrides[preset.name];
  if (!override) {
    continue;
  }
  preset.words = uniqueWords(override.words);
  preset.description = override.description;
  updatedCount++;
}

const existingIds = new Set(presets.map((preset) => preset.id));
let addedCount = 0;
for (const addition of additions) {
  if (existingIds.has(addition.id)) {
    continue;
  }
  addition.words = uniqueWords(addition.words);
  presets.push(addition);
  existingIds.add(addition.id);
  addedCount++;
}

writeFileSync(path, `${JSON.stringify(presets, null, 2)}\n`);
console.log(`Updated ${updatedCount} presets and added ${addedCount} presets in ${path}`);
